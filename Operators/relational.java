public class relational{
    public static void main(String [] args)throws Exception
    {
        int a =10, b=5;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a<b);
        System.out.println(a>b);
    }
}